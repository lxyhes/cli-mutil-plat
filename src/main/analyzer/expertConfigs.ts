/** 根据类型获取专家配置 */
export function getExpertConfig(type: string): ExpertConfig | undefined {
  return EXPERT_CONFIGS.find(c => c.type === type)
}

/** 获取所有启用的专家配置 */
export function getEnabledExperts(): ExpertConfig[] {
  return EXPERT_CONFIGS.filter(c => c.enabled)
}
